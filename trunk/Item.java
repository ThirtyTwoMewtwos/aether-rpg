
public abstract class Item 
{
    String name, description;
    
    public String getName()
    {
        return name;
    }
    public void setName(String itemName)
    {
        name = itemName;
    }
    public String getDescription()
    {
        return description;
    }
    public void setDescription(String desc)
    {
        description = desc;
    }
        
}
